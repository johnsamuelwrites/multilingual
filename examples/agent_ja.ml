# 多言語AIエージェント — 日本語
# agent_en.ml と agent_fr.ml と同じエージェントロジック。
# Multilingualが、エージェントコードがどの人間言語でも
# 慣用的である唯一のAIプラットフォームであることを証明する。
#
# 実演する機能:
#   @ツール, @エージェント, 考える, 尋ねる, 並列 [ ... ], 起動, 使う 効果

@ツール(説明="現在の情報をウェブで検索する")
関数 ウェブ検索(クエリ: str) -> str 使う ネット:
    パス

@ツール(説明="数式を計算する")
関数 計算する(式: str) -> str:
    パス

@エージェント(モデル=@claude-sonnet)
関数 調査エージェント(質問: str) -> str 使う ai, ネット:
    # まず考える：質問を並列サブクエリに分解する
    変数 計画 = 考える @claude-sonnet:
        この調査質問を、並列処理可能な2つの独立したサブクエリに分解する。
        質問: 質問

    # 並列を使って両方のサブクエリを同時実行する
    変数 結果群 = 並列 [
        尋ねる @claude-sonnet: 計画.結論 + "\n事実に集中する。",
        尋ねる @claude-sonnet: 計画.結論 + "\n文脈に集中する。"
    ]

    # 並列結果を統合して最終回答を生成する
    変数 回答 = 尋ねる @claude-sonnet:
        "これら2つの調査スレッドを1つの明確な回答にまとめる：\n"
        + 結果群[0] + "\n---\n" + 結果群[1]

    返す 回答

関数 メイン():
    変数 結果 = 調査エージェント("東京の人口は何人ですか？")
    表示(結果)
